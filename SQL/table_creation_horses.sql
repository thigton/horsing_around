CREATE TABLE horses (
horse_ID VARCHAR(500) PRIMARY KEY,
race_ID VARCHAR(500) ,
form VARCHAR(500) ,
horse_weight VARCHAR(500) ,
days_since_last_run INTEGER ,
horse_age INTEGER ,
jockey_name VARCHAR(500) ,
jockey_form VARCHAR(500) ,
trainer_name VARCHAR(500) ,
stall INTEGER ,
horse_analysis_text VARCHAR(500) ,
horse_added_timestamp TIMESTAMP)